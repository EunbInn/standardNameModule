package standardName;

public class Result {
	private String cu;
	private String gs;

	public Result() {
		this.cu = "";
		this.gs = "";
	}
	
	public String getCu() {
		return cu;
	}

	public void setCu(String cu) {
		this.cu = cu;
	}

	public String getGs() {
		return gs;
	}

	public void setGs(String gs) {
		this.gs = gs;
	}

}
